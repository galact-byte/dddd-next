func probeMemcached(ctx context.Context, host string, port int, timeout time.Duration) (*types.Finding, error) {
	conn, err := net.DialTimeout("tcp", net.JoinHostPort(host, strconv.Itoa(port)), timeout)
	if err != nil {
		return nil, err
	}
	defer conn.Close()
	_ = conn.SetDeadline(time.Now().Add(timeout))

	if _, err := conn.Write([]byte("stats\r\n")); err != nil {
		return nil, err
	}
	buf := make([]byte, 1024)
	n, err := conn.Read(buf)
	if err != nil || !bytes.Contains(buf[:n], []byte("STAT ")) {
		return nil, err
	}
	return &types.Finding{
		ID:           "memcached-unauthorized",
		Name:         "Memcached Unauthorized Access",
		Severity:     types.SeverityHigh,
		Target:       net.JoinHostPort(host, strconv.Itoa(port)),
		Description:  "Memcached answers the stats command without authentication",
		Tags:         []string{"memcached", "unauthorized"},
		DiscoveredAt: time.Now(),
	}, nil
}

func probeADB(ctx context.Context, host string, port int, timeout time.Duration) (*types.Finding, error) {
	conn, err := net.DialTimeout("tcp", net.JoinHostPort(host, strconv.Itoa(port)), timeout)
	if err != nil {
		return nil, err
	}
	defer conn.Close()
	_ = conn.SetDeadline(time.Now().Add(timeout))

	if _, err := conn.Write(adbConnectMsg); err != nil {
		return nil, err
	}
	buf := make([]byte, 4096)
	n, err := conn.Read(buf)
	if err != nil || n < 4 || string(buf[:4]) != "CNXN" {
		return nil, err
	}
	return &types.Finding{
		ID:           "adb-unauthorized",
		Name:         "ADB Unauthorized Access",
		Severity:     types.SeverityCritical,
		Target:       net.JoinHostPort(host, strconv.Itoa(port)),
		Description:  "Android Debug Bridge accepts connections without authorization (RCE-equivalent)",
		Tags:         []string{"adb", "unauthorized", "rce"},
		DiscoveredAt: time.Now(),
	}, nil
}

func probeJDWP(ctx context.Context, host string, port int, timeout time.Duration) (*types.Finding, error) {
	conn, err := net.DialTimeout("tcp", net.JoinHostPort(host, strconv.Itoa(port)), timeout)
	if err != nil {
		return nil, err
	}
	defer conn.Close()
	_ = conn.SetDeadline(time.Now().Add(timeout))

	// JDWP has no auth: if the server echoes the handshake, debug access is open.
	if _, err := conn.Write([]byte("JDWP-Handshake")); err != nil {
		return nil, err
	}
	buf := make([]byte, 1024)
	n, err := conn.Read(buf)
	if err != nil || !bytes.Contains(buf[:n], []byte("JDWP-Handshake")) {
		return nil, err
	}
	return &types.Finding{
		ID:           "jdwp-unauthorized",
		Name:         "JDWP Unauthorized Access",
		Severity:     types.SeverityCritical,
		Target:       net.JoinHostPort(host, strconv.Itoa(port)),
		Description:  "Java Debug Wire Protocol is exposed without authentication (RCE-equivalent)",
		Tags:         []string{"jdwp", "unauthorized", "rce"},
		DiscoveredAt: time.Now(),
	}, nil
}
